<template>
	<div class="assistant-form">
		<span class="assistant-bubble">
			<CreationIcon :size="16" class="icon" />
			<span>{{ t('assistant', 'Blablador Assistant') }}</span>
		</span>
		<TaskTypeSelect
			:value.sync="mySelectedTaskTypeId"
			class="task-custom-select"
			:inline="3"
			:options="taskTypes" />
		<h2 v-if="selectedTaskType"
			class="task-name">
			{{ selectedTaskType.name }}
		</h2>
		<span v-if="selectedTaskType"
			class="task-description">
			{{ selectedTaskType.description }}
		</span>
		<label for="assistant-input"
			class="input-label">
			{{ t('assistant', 'Input') }}
		</label>
		<NcRichContenteditable
			id="assistant-input"
			:value.sync="myInput"
			class="editable-input"
			:multiline="true"
			:disabled="loading"
			:placeholder="t('assistant', 'Type some text')"
			:link-autocomplete="false" />
		<div v-if="myOutput"
			class="output">
			<label
				for="assistant-output"
				class="input-label">
				{{ t('assistant', 'Result') }}
			</label>
			<NcRichContenteditable
				id="assistant-output"
				ref="output"
				:value.sync="myOutput"
				class="editable-output"
				:multiline="true"
				:disabled="loading"
				:placeholder="t('assistant', 'Result')"
				:link-autocomplete="false" />
			<NcNoteCard
				class="warning-note"
				type="warning">
				{{ t('assistant', 'This output was generated by AI. Make sure to double-check and adjust.') }}
			</NcNoteCard>
		</div>
		<div class="footer">
			<NcButton
				v-if="showSubmit"
				:type="submitButtonType"
				class="submit-button"
				:disabled="!canSubmit"
				:title="t('assistant', 'Run a task')"
				@click="onSyncSubmit">
				{{ syncSubmitButtonLabel }}
				<template #icon>
					<NcLoadingIcon v-if="loading" />
					<CreationIcon v-else />
				</template>
			</NcButton>
			<!--NcButton
				v-if="showSubmit"
				:type="submitButtonType"
				class="submit-button"
				:disabled="!canSubmit"
				:aria-label="t('assistant', 'Schedule an assistant task')"
