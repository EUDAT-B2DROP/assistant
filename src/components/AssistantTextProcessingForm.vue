				</template>
			</NcButton-->
			<NcButton
				v-if="hasOutput"
				type="primary"
				class="copy-button"
				:title="t('assistant', 'Copy output')"
				@click="onCopy">
				{{ t('assistant', 'Copy') }}
				<template #icon>
					<ClipboardCheckOutlineIcon v-if="copied" />
					<ContentCopyIcon v-else />
				</template>
			</NcButton>
			<div v-if="hasOutput"
				class="action-buttons">
				<NcButton
					v-for="(b, i) in actionButtons"
					:key="i"
					:type="b.type ?? 'secondary'"
					:title="b.title"
					@click="onActionButtonClick(b)">
					{{ b.label }}
					<template v-if="b.iconSvg" #icon>
						<NcIconSvgWrapper :svg="b.iconSvg" />
					</template>
				</NcButton>
			</div>
		</div>
	</div>
</template>

<script>
import ContentCopyIcon from 'vue-material-design-icons/ContentCopy.vue'
import ClipboardCheckOutlineIcon from 'vue-material-design-icons/ClipboardCheckOutline.vue'
import CreationIcon from 'vue-material-design-icons/Creation.vue'

import NcLoadingIcon from '@nextcloud/vue/dist/Components/NcLoadingIcon.js'
import NcButton from '@nextcloud/vue/dist/Components/NcButton.js'
import NcRichContenteditable from '@nextcloud/vue/dist/Components/NcRichContenteditable.js'
import NcNoteCard from '@nextcloud/vue/dist/Components/NcNoteCard.js'
import NcIconSvgWrapper from '@nextcloud/vue/dist/Components/NcIconSvgWrapper.js'

import TaskTypeSelect from './TaskTypeSelect.vue'

import axios from '@nextcloud/axios'
import { generateOcsUrl } from '@nextcloud/router'
import { showError } from '@nextcloud/dialogs'
import VueClipboard from 'vue-clipboard2'
import Vue from 'vue'

Vue.use(VueClipboard)

const FREE_PROMPT_TASK_TYPE_ID = 'OCP\\TextProcessing\\FreePromptTaskType'

export default {
	name: 'AssistantTextProcessingForm',
	components: {
		TaskTypeSelect,
		NcButton,
		NcRichContenteditable,
		NcLoadingIcon,
		NcIconSvgWrapper,
		CreationIcon,
		ContentCopyIcon,
		ClipboardCheckOutlineIcon,
		NcNoteCard,
	},
	props: {
		loading: {
			type: Boolean,
			default: false,
		},
		input: {
			type: String,
			default: '',
		},
		output: {
			type: String,
			default: '',
		},
		selectedTaskTypeId: {
			type: [String, null],
			default: null,
		},
		actionButtons: {
			type: Array,
			default: () => [],
		},
	},
	emits: [
		'sync-submit',
		'submit',
		'action-button-clicked',
	],
	data() {
		return {
			myInput: this.input,
			myOutput: this.output,
			taskTypes: [],
			mySelectedTaskTypeId: this.selectedTaskTypeId || FREE_PROMPT_TASK_TYPE_ID,
			copied: false,
		}
	},
	computed: {
		selectedTaskType() {
			if (this.mySelectedTaskTypeId === null) {
				return null
			}
			return this.taskTypes.find(tt => tt.id === this.mySelectedTaskTypeId)
		},
		submitButtonType() {
			return this.myOutput.trim() ? 'secondary' : 'primary'
		},
		showSubmit() {
			return this.selectedTaskType
		},
		canSubmit() {
			return this.selectedTaskType && !!this.myInput.trim()
		},
		submitButtonLabel() {
			return this.myOutput.trim()
				? t('assistant', 'Try again')
				: this.selectedTaskType.id === FREE_PROMPT_TASK_TYPE_ID
					? t('assistant', 'Send request')
					: this.selectedTaskType.name
		},
		syncSubmitButtonLabel() {
			return this.myOutput.trim()
				? t('assistant', 'Try again')
				: this.selectedTaskType.id === FREE_PROMPT_TASK_TYPE_ID
					? t('assistant', 'Send request')
					: this.selectedTaskType.name
		},
		hasOutput() {
			return !!this.myOutput.trim()
		},
	},
	watch: {
		output(newVal) {
			this.myOutput = newVal
		},
	},
	mounted() {
		this.getTaskTypes()
	},
	methods: {
		getTaskTypes() {
			axios.get(generateOcsUrl('textprocessing/tasktypes', 2))
				.then((response) => {
					this.taskTypes = response.data.ocs.data.types
				})
				.catch((error) => {
					console.error(error)
				})
		},
		onSubmit() {
			this.$emit('submit', { input: this.myInput.trim(), taskTypeId: this.mySelectedTaskTypeId })
		},
		onSyncSubmit() {
			this.$emit('sync-submit', { input: this.myInput.trim(), taskTypeId: this.mySelectedTaskTypeId })
		},
		async onCopy() {
			try {
				const container = this.$refs.output.$el
				await this.$copyText(this.myOutput.trim(), container)
				this.copied = true
				setTimeout(() => {
					this.copied = false
				}, 5000)
			} catch (error) {
				console.error(error)
				showError(t('assistant', 'Result could not be copied to clipboard'))
			}
		},
		onActionButtonClick(button) {
			this.$emit('action-button-clicked', { button, output: this.myOutput.trim() })
		},
	},
}
</script>

